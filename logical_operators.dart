void main() {

  // Relational operators: ==, !=, >, <, >=, <=
  int n = 10;
  int n2 = 13;

  // bool type -> true or false
  bool r = n == 10;
  print(r); // true

  r = n != 10;
  print(r); // false

  r = n > n2;
  print(r); // false

  r = n < n2;
  print(r); // true

  // Logical operators: && || !
  

}